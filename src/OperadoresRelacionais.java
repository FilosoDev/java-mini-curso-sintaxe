
public class OperadoresRelacionais {

	public static void main(String[] args) {
		
		// == igualdade
		System.out.println(1 == 2);

		// != diferença
		System.out.println(1 != 2);
		
		// > maior
		System.out.println(1 > 0);
		
		// < menor
		System.out.println(1 < 0);
		
		// >= maior ou igual
		System.out.println(1 >= 1);
		
		// <= menor ou igual
		System.out.println(1 <= 2);
	}

}
